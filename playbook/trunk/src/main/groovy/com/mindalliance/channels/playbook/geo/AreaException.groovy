class AreaException extends RuntimeException {

    AreaException() {
        super()
    }

    AreaException(String message) {
        super(message)
    }

    AreaException(Throwable cause) {
        super(cause)
    }

    AreaException(String message, Throwable cause) {
        super(message, cause)
    }

}