<richcontent TYPE="NOTE"><html>
  <head>
    
  </head>
  <body>
    <p>
      The process is made up of activities (e.g. Describe organization, FInd an exchange requirement etc.). Activities use information already in the model and add to the model. Activities are organized into 5 groups. Note that the groups are <b>not</b> &quot;phases&quot;. There is no a priori prescribed order in which to do activities. Activities can depend on the output of other activities, so some sequencing is implied. For example one can not find information flows satisfying an exchange requirement that has yet to be found and sufficiently described.
    </p>
    <p>
      There are best practices (to be documented elsewhere) that help find a productive sequence of activities.
    </p>
