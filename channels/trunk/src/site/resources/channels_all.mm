<node CREATED="1172216097648" ID="Freemind_Link_1724185557" MODIFIED="1172241524019" TEXT="In">
<richcontent TYPE="NOTE"><html>
  <head>
    
  </head>
  <body>
    <p>
      Channels 1.0 is a communication planning and preparedness Web application. It supports collaborative role-based profiling of responsibilities and the exploration of concrete, what-if scenarios with the focus on capturing assertions of information needs, availability and accessibility. This knowledge is organized as information sharing models that are continuously analyzed to discover information sharing opportunities (exchanges), communication flows that could satisfy them and issues that jeopardize the timely and secure satisfaction of information needs. Guided impact analysis reveals the likely consequences of not resolving these issues.
    </p>
    <p>
      While users engage in concrete scenario and profile building, Channels 1.0 uses automated deductive reasoning to assist in the discovery of implied knowledge such as exchanges, issues and even of what the &quot;next steps&quot; could be for project participants. Channels 1.0 also uses inductive reasoning (going specific to generic) to uncover &quot;templates&quot; that capture generalized model elements that can be applied across scenarios (such as the apparent responsabilities of any County Public Health Officer). Channels 1.0 uses pre-defined templates (implementing standards) and induced templates (the system learning from participants) to offer suggestions to project participants. The objective is for scenario exploration to be largely guided by suggestions offered by both the system and participants.
    </p>
    <p>
      The principal deliverables of a Channels project are:
    </p>
    <ul>
      <li>
        A continuously growing and updated collection of scenarios and reusable profiles
      </li>
      <li>
        Reusable templates either defined as standards or extracted from the scenarios
      </li>
      <li>
        Printable reports that aggregate the content and analysis of scenarios
      </li>
      <li>
        Dashboards that provide a metrics-based overview of communication preparedness and capabilities
      </li>
    </ul>
  </body>
</html>
