      The Meta information for a model element (what can be communicated about the element) is a combination of the meta information associated with the kind of element (e.g. Task - what can be communicated about any task?), its types (e.g. &quot;autopsy&quot; and thus &quot;medical examination&quot; - what can be communicated about autopsies specifically and medical examinations in general) and whatever ad hoc information could be communicated about the specific entity (here, a task) being described in the model (the autopsy of John Doe - what else can be communicated about this specific autopsy?).
