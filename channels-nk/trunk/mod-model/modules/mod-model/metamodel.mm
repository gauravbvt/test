<node CREATED="1192288359549" ID="Freemind_Link_1604482253" MODIFIED="1193948260968" TEXT="Information">
<richcontent TYPE="NOTE"><html>
  <head>
    
  </head>
  <body>
    <p>
      What can be known and communicated about an entity.
    </p>
    <p>
      An information is composed of elements of information (EOIs). EOIs are grouped into Aspects. An Aspect can have sub-Aspects. Each Aspect is a sense identifying an aspect of the entity being described. EOIs are typed name-value pairs. They represent attributes of the entity as a whole or, when contained in an Aspect, attributes of that aspect of the entity.
    </p>
    <p>
      An EOI is defined by a sense (what the meaning of the attribute is), a type for the attribute's value (also a sense) and an optional value.
    </p>
    <p>
      An information can be thought of as a pattern (not all possible EOIs need be defined and not all EOIs need be valued) that describes a class of actual, &quot;real-world&quot; information.
    </p>
    <p>
      Information can be about an entity that is itself defined by an information, for example Knowledge (information held by an agent) or a Message (information encoded for transmission). Information about such entities will contain a description of the information that defines them. Information describing information is &quot;meta-information&quot;.
    </p>
    <p>
      <u>Meta-information</u><br />
    </p>
    <p>
      This is information *about* information and its aspects and EOIs:
    </p>
    <p>
      - one aspect has sense &quot;entity#1&quot; (what the info is about)
    </p>
    <p>
      -- its EOI is:
    </p>
    <p>
      --- sense = &quot;identity&quot;, type =&quot;identifier#1&quot;, optional value = the identity of an element (GUID)
    </p>
    <p>
      - it has (sub) aspect with sense &quot;category#1&quot; (a set)
    </p>
    <p>
      -- its EOI is:
    </p>
    <p>
      --- sense = &quot;sense#2&quot;, type = &quot;sense identity&quot; (class of entities), value = a sense ID
    </p>
    <p>
      <i>*** the following is implied by the categories of the entity the information is about ***</i>
    </p>
    <p>
      another aspect has sense &quot;aspect#1&quot; (a set)
    </p>
    <p>
      -- its EOIs are:
    </p>
    <p>
      --- sense = &quot;sense#2&quot;, type = &quot;sense identity&quot; (meaning of the aspect), value = a sense ID
    </p>
    <p>
      --- sense = &quot;parent aspect#1&quot;, type = &quot;sense identity&quot; (meaning of parent aspect), value = a sense ID
    </p>
    <p>
      -- it has a set (sub)aspect with sense &quot;attribute#2&quot; -- EOI
    </p>
    <p>
      --- its EOIs are:
    </p>
    <p>
      ----- sense = &quot;sense#2&quot;, type = &quot;sense identity&quot; (meaning of an EOI), value = a sense ID
    </p>
    <p>
      ----- [optional] sense = &quot;type#?&quot;, type = &quot;sense identity&quot; (type of the EOI value, as sense), value = a sense ID
    </p>
    <p>
      ----- [optional] sense = &quot;value&quot;, type = &quot;sense identity&quot; (from above), value = some value
    </p>
    <p>
      <u>Meta or meta-meta?</u>
    </p>
    <p>
      Meta is a relative qualifier, not an absolute one.
    </p>
    <p>
      At reality level, the information is either &quot;actual&quot; information (&quot;The king is dead!&quot;) or meta-information (&quot;Notify me of changes in the king's health&quot;).
    </p>
    <p>
      At modeling level, the information is either meta-information (&quot;Information about the king's health is requested&quot;) and meta-meta information (&quot;Information about the king's health includes life status&quot;).
    </p>
    <p>
      Another way of thinking about it is:
    </p>
    <ul>
      <li>
        Actual information (known or communicated &quot;data&quot;) is a filled-in form (name-value pairs organized by aspects).
      </li>
      <li>
        Meta-information (modeled actual information, requested information) is a structured form (aspects, sub-aspects) to be filled in, with validity-constrained entry fields (EOIs).
      </li>
      <li>
        Meta-meta-information (information about a category of information) is a description of a form (listing it's aspects and EOIs).
      </li>
    </ul>
  </body>
