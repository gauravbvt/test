<node CREATED="1193144720527" ID="Freemind_Link_768817335" MODIFIED="1194017724663" TEXT="Knowledge">
<richcontent TYPE="NOTE"><html>
  <head>
    
  </head>
  <body>
    <p>
      The holding of information by an agent. The agent has more or less confidence in the correctness of the information.
    </p>
    <p>
      Knowledge can be negated to represent absence of knowledge.
    </p>
    <p>
      Knowledge models the fact that an agent holds some information, possibly obtained from another agent. The agent has some level of confidence in the correctness of the information.
    </p>
    <p>
      A transmission of known information causes a knowledge event in the receiver (assuming the information, if encrypted, can be decrypted by receiver). Upon receipt, the receiver knows the information but may have a level of confidence different from that of the sender.
    </p>
    <p>
      <u>Meta-knowledge: knowing that some agent knows some information</u>
    </p>
    <p>
      Meta-knowledge is an agent knowing that another agent knowns some information. Meta-knowledge is an important dimension of information sharing; an agent should not transmit information it knows to be already known to the receiver, and the agent should try to only query sources it knows has the needed information.
    </p>
    <p>
      What can be known about knowing, i.e. what information can be communicated to convey that &quot;X knows (about) Y&quot;? Knowledge can be categorized (being an entity); a valid category has sense with hypernym = &quot;knowledge#1&quot;. The information attached to the knowledge category describes what is known. A top EOI is &quot;confidence&quot;. The top topics are &quot;agent&quot; (who knows), &quot;informant&quot; (from whom) and &quot;information&quot; (what is known). The EOIs and sub-topics of &quot;agent&quot; are those describing the agent (from information attached to its categories). The EOIs and sub-topics of &quot;source&quot; describe the provenance of the information. The topics &quot;information&quot; describe the information (that describes an entity) and is meta-information implied by (the information attached to) the categories of the entity that is the subject of this knowledge.
    </p>
    <p>
      So an agent can hold information about another agent holding information, aka, know with some confidence that another agent knows something about an entity, without knowing anything about the entity per se.
    </p>
  </body>
</html>
</richcontent>
