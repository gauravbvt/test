<node CREATED="1192287991719" ID="Freemind_Link_460696537" MODIFIED="1193601460564" TEXT="Information Flow Modeling">
<richcontent TYPE="NOTE"><html>
  <head>
    
  </head>
  <body>
    <p>
      Information flow modeling is the process by which potential information flows are specified within scenarios of causally connected events that involve resources and other entities (entities are elements that can be known to agents).
    </p>
    <p>
      Events are either stated (true by fiat), or they are implied (true by inference) or suggested (not true until stated). Implied and suggested events are produced by information modeling rules and are subject to truth maintenance (automatically revised when the model changes).
    </p>
    <p>
      An entity that is defined outside of the scope of an event is considered to be permanent within the scope any scenario in a project. An entity that is redefined within the scope of an event overrides its permanent definition for the duration of the event. A new entity defined within the scope of an event exists only for the duration of the event.
    </p>
    <p>
      Model elements can exist independently of other entities and have unique, referenceable identities. Entities are a special kind of element. Components are parts of elements and do not exist independently of the elements they compose.
    </p>
    <p>
      Application objects are either building blocks or outcomes of an information flow modeling application.
    </p>
  </body>
</html>
</richcontent>
