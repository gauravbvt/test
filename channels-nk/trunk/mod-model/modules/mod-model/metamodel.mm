<node CREATED="1192561066887" ID="Freemind_Link_1914577952" MODIFIED="1194014580005" TEXT="Linked event">
<richcontent TYPE="NOTE"><html>
  <head>
    
  </head>
  <body>
    <p>
      An event which is triggered and possibly terminated by the starting or ending of events.
    </p>
    <p>
      No cycles are allowed. The event relationships form a directed, acyclic graph.
    </p>
    <p>
      The default trigger of an event is the start of the scenario it is in (at time zero). The default terminator of an event is its own start (an event auto-terminates by default). The default delay is 0.
    </p>
    <p>
      Each boundary has a probable effectiveness (as trigger or terminator). The default is 1.
    </p>
    <p>
      Multiple events sharing the same trigger (triggered by same event boundary) can be grouped to specify that they are mutually exclusive. The sum of the triggers' probabilities for grouped events must be &lt;= 1. Mutually exclusive events start alternate event flows.
    </p>
  </body>
</html>
</richcontent>
